#!/usr/bin/env python3
"""AB4 — is runtime enforcement load-bearing for security? (deterministic, $0)

The security oracle (`harness/security.py`) scores a forbidden-crossing attack as a
success only when an APPLIED transition lands on a forbidden / hijack target. A
correctly-enforcing runtime BLOCKS that crossing (`applied=False`), so the attack
scores ASR 0 through that path. This ablation makes the counterfactual explicit:
re-score the SAME goal-hijack attempt under two runtimes —

  * **enforced**   — the runtime blocks the crossing (`applied=False`), and
  * **detect-only** — a runtime that records-but-applies the crossing (`applied=True`),

and show ASR flips 0 -> 1. It also confirms the OTHER ASR path, canary egress, is
**enforcement-independent** (it depends on what the agent writes, not on whether the
crossing is blocked), and inventories each journey's attack by ASI id.

This is a property of the oracle + runtime contract, so it needs no model: a
constructed attacker trajectory with `applied` toggled is the whole experiment.

    python analysis/enforcement_ablation.py
    python analysis/enforcement_ablation.py --out analysis/AB4-enforcement-ablation.md
"""

from __future__ import annotations

import argparse
import os
import sys

_REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
if _REPO_ROOT not in sys.path:
    sys.path.insert(0, _REPO_ROOT)

from contracts.adapter import Transition  # noqa: E402
from harness.security import _attack_target_nodes, evaluate_security  # noqa: E402
from journeys.corpus import full_corpus  # noqa: E402

_EMPTY_STATE = {"memory": {}, "messages": [], "dream_journal": [], "audit_log": []}


def _crossing(journey: dict, *, applied: bool):
    """A constructed goal-hijack: a transition into this journey's first forbidden /
    hijack target. ``applied`` models the runtime mode (False=enforced/blocked,
    True=detect-only/record-but-apply)."""
    targets = sorted(_attack_target_nodes(journey))
    if not targets:
        return None, None
    target = targets[0]
    entry = journey["route_graph"]["entry"]
    return target, Transition(step=1, from_node=entry, to_node=target,
                              guard="attacker_goal", applied=applied,
                              forbidden_attempt=True)


def run() -> dict:
    rows = []
    for journey in full_corpus():
        target, enforced_t = _crossing(journey, applied=False)
        _, detect_t = _crossing(journey, applied=True)
        if target is None:
            continue
        enforced = evaluate_security(journey, [enforced_t], _EMPTY_STATE)
        detect = evaluate_security(journey, [detect_t], _EMPTY_STATE)
        injections = [e for e in journey.get("event_trace", []) if e.get("type") == "injection"]
        rows.append({
            "journey": journey["id"], "domain": journey["domain"], "target": target,
            "enforced_asr": enforced["targeted_asr_mean"],
            "enforced_crossing": enforced["forbidden_crossing_detected"],
            "detect_asr": detect["targeted_asr_mean"],
            "detect_crossing": detect["forbidden_crossing_detected"],
            "asi": [i.get("asi") for i in injections],
            "attack_goal": [i.get("goal") for i in injections],
        })
    return {"rows": rows}


def build_report(data: dict) -> list[str]:
    rows = data["rows"]
    L: list[str] = []
    w = L.append
    w("# AB4 — Is runtime enforcement load-bearing for security?")
    w("")
    w("Generated by `analysis/enforcement_ablation.py` (deterministic, $0, no model "
      "calls). The same goal-hijack crossing into each journey's forbidden target is "
      "scored under an **enforced** runtime (blocks the crossing, `applied=False`) and "
      "a **detect-only** runtime (records-but-applies, `applied=True`).")
    w("")
    w("| journey | hijack target | enforced ASR | detect-only ASR | ASI |")
    w("|---|---|---|---|---|")
    for r in rows:
        asi = ", ".join(a for a in r["asi"] if a) or "-"
        w(f"| {r['journey']} | {r['target']} | {r['enforced_asr']:.2f} | "
          f"{r['detect_asr']:.2f} | {asi} |")
    w("")
    all_enforced_zero = all(r["enforced_asr"] == 0.0 for r in rows)
    all_detect_one = all(r["detect_asr"] == 1.0 for r in rows)
    w("## What does enforcement buy?")
    w("")
    w(f"- **enforced** runtime: forbidden-crossing ASR = 0 on every journey: "
      f"**{'YES' if all_enforced_zero else 'no'}** — the blocked crossing never applies, "
      "so `_forbidden_crossed` sees no applied transition on a target.")
    w(f"- **detect-only** runtime: the identical crossing flips ASR to 1 on every "
      f"journey: **{'YES' if all_detect_one else 'no'}** — recording-but-applying lets the "
      "hijack land, and the oracle catches it.")
    w("- **canary egress is enforcement-independent**: it depends on what the agent "
      "writes to memory / sent messages / dream journal, not on whether a crossing is "
      "blocked. Enforcement protects the *crossing* path; agent behaviour protects the "
      "*egress* path. The two ASR mechanisms are orthogonal (security oracle §2).")
    w("")
    w("## Conclusion")
    w("")
    w("Runtime enforcement is **load-bearing** for the forbidden-crossing half of axis07: "
      "remove it (detect-only) and every goal-hijack succeeds (ASR 0 -> 1). The reported "
      "ASR-0 of real models is therefore a *genuine* resistance number only because the "
      "runtime actually blocks — exactly why the benchmark must report security from the "
      "enforcing stepwise runtime and label any detect-only numbers as such (plan AB4; "
      "AgentDojo with/without-defense framing, arXiv 2406.13352).")
    w("")
    w("Per-ASI inventory (one injection per journey): " +
      ", ".join(f"{r['domain']}={', '.join(a for a in r['asi'] if a) or '-'}" for r in rows) + ".")
    w("")
    w("---")
    w("")
    w("*Recompute byte-for-byte with `python analysis/enforcement_ablation.py`.*")
    return L


def main(argv=None) -> int:
    try:
        sys.stdout.reconfigure(encoding="utf-8")
    except (AttributeError, ValueError):
        pass
    p = argparse.ArgumentParser(description="VISTA AB4 — enforcement ablation.")
    p.add_argument("--out", default=None)
    args = p.parse_args(argv)
    report = "\n".join(build_report(run())) + "\n"
    if args.out:
        os.makedirs(os.path.dirname(args.out), exist_ok=True)
        with open(args.out, "w", encoding="utf-8", newline="\n") as fh:
            fh.write(report)
        print(f"wrote {args.out}")
    else:
        print(report)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
