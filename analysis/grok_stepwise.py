#!/usr/bin/env python3
"""Statistically-solid placement of grok-build on the stepwise (pass^k) seam.

The single-journey, k=5 pass^k run showed grok-build reaching the goal 0/5 in the
turn-by-turn Worker seam while matching the reference Ranger in the one-shot planning
seam. 0/5 has a Wilson 95% interval of [0, 43%] on one journey — a behavioural hint,
not a publishable claim. This upgrades it to a defensible result with three runs (see
`experiments/passk_run.py`, all deterministic-scorer, $0 on the native Grok CLI):

  * **depth**    — k=20 on the canonical cliff journey (tight per-journey CI);
  * **breadth**  — k=5 on one easy-tier journey per domain (6 domains) → does the
                   stall *generalize*, or is it specific to the seed journey?
  * **confound** — k=10 on the canonical journey in the action-permitting `default`
                   permission mode (vs `plan`) → is plan-mode disposition the cause,
                   or does grok stall even when free to act?

Every rate carries a Wilson 95% score interval (robust at the 0/n boundary); pass^k
carries the tau-bench unbiased estimator C(c,k)/C(n,k) plus a fixed-seed percentile
bootstrap — the same estimators `analysis/analyze.py` uses, so the whole thing stays
pure-stdlib, deterministic, and reproducible (NFR-1).

    python analysis/grok_stepwise.py            # writes analysis/GROK-STEPWISE-STATS.md
"""

from __future__ import annotations

import json
import os
import sys

_REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
if _REPO_ROOT not in sys.path:
    sys.path.insert(0, _REPO_ROOT)

# The report contains non-ASCII (✓/✗/–); force UTF-8 so printing it doesn't crash on
# a cp1252 Windows console (the file is always written UTF-8 regardless).
if hasattr(sys.stdout, "reconfigure"):
    sys.stdout.reconfigure(encoding="utf-8", errors="replace")

from analysis.analyze import (  # noqa: E402
    wilson, pass_hat_k, bootstrap_passk_ci, _BOOTSTRAP_ITERS, _BOOTSTRAP_SEED,
)

_STUDY = os.path.join(_REPO_ROOT, "results", "grok-stepwise-study")
_PASSK = os.path.join(_REPO_ROOT, "results", "pillar-a-passk")
_OUT = os.path.join(_REPO_ROOT, "analysis", "GROK-STEPWISE-STATS.md")

# The committed k=5 single-journey baselines (the published leaderboard set).
_BASELINES = [
    ("gemini-2.5-pro", "Gemini 2.5 Pro"),
    ("gemini-2.5-flash", "Gemini Flash"),
    ("sonnet", "Claude Sonnet 4.6"),
    ("opus", "Claude Opus 4.8"),
    ("haiku", "Claude Haiku 4.5"),
]


def _load(path):
    try:
        with open(path, "r", encoding="utf-8") as fh:
            return json.load(fh)
    except (OSError, json.JSONDecodeError):
        return None


def _pct(x):
    return f"{100 * x:.0f}%"


def _wil(c, n):
    """'p% (95% CI lo–hi, n=N)' from a Wilson score interval."""
    if not n:
        return "n/a"
    lo, hi = wilson(c, n)
    return f"{_pct(c / n)} (95% CI {_pct(lo)}–{_pct(hi)}, n={n})"


def _vecs(row):
    """(goal_vec, pass_vec) for a passk_run result row."""
    a = row["agg"]
    return a["goal_vec"], a["pass_vec"]


def main() -> int:
    depth = _load(os.path.join(_STUDY, "depth.json"))
    breadth = _load(os.path.join(_STUDY, "breadth.json"))
    confound = _load(os.path.join(_STUDY, "confound.json"))
    if depth is None:
        print("no depth.json yet — run experiments/passk_run.py first", file=sys.stderr)
        return 1

    L: list[str] = []
    w = L.append

    w("# Grok-build on the stepwise seam — a statistically-solid placement\n")
    w("*Generated by `analysis/grok_stepwise.py` (pure-stdlib, fixed-seed bootstrap, "
      "deterministic scorer — NFR-1). $0: native Grok CLI, no metered tokens.*\n")
    w("**Headline.** grok-build plans like the reference agent in one shot, but its "
      "turn-by-turn *execution* is **domain-dependent** and tracks its coding "
      "specialization: it completes the stepwise task reliably only in the **coding** "
      "domain (5/5) and stalls or flakes in the other five white-collar domains (0–20%). "
      "On the canonical project journey it reaches the goal 0/20 (Wilson 95% [0, 16%]) — "
      "the Opus/Haiku bucket — and the stall survives the plan-mode confound. Capability "
      "and price do not predict long-horizon execution; **domain specialization does** — "
      "the same model is leaderboard-topping in one domain and bottom-of-cliff in the "
      "rest, a within-model split a single pooled rate would hide.\n")

    # ---- Methodology -------------------------------------------------------- #
    w("## Method\n")
    w("- **Harness**: `StepwiseAdapter` drives the model one action per turn through "
      "the same VISTA scorer used for every other model — no LLM judge, no RNG, no "
      "wall-clock. The model is the only stochastic element.\n")
    w("- **Metric**: `goal_reach_rate` = fraction of k runs that reach the *intended* "
      "absorbing state; `pass^k` = the tau-bench unbiased estimator C(c,k)/C(n,k).\n")
    w(f"- **Intervals**: Wilson 95% score interval on each rate (robust at the 0/n "
      f"boundary); pass^k bootstrap = {_BOOTSTRAP_ITERS} resamples, seed {_BOOTSTRAP_SEED}.\n")
    w("- **Baseline**: the deterministic Ranger's plan, replayed through the *same* "
      "stepwise adapter, is reported per journey as the reachable-in-budget ceiling.\n")

    # ---- Result 1: depth on the canonical journey --------------------------- #
    drow = depth["results"][0]
    gv, pv = _vecs(drow)
    n = len(gv)
    cg = sum(gv)
    rng = drow["ranger"]
    w("## 1 · Depth — canonical cliff journey, k=20 (plan mode)\n")
    w(f"Journey `{drow['journey']}` ({drow['domain']}), `max_steps={depth['max_steps']}`.\n")
    w("| agent | goal-reach rate (Wilson 95%) | recall | targeted ASR |\n")
    w("|---|---|---|---|\n")
    w(f"| **grok-build** (k={n}) | **{_wil(cg, n)}** | "
      f"{drow['agg']['recall_mean']:.2f} | {drow['agg']['asr_mean']:.2f} |\n")
    w(f"| Ranger (deterministic) | {_pct(1.0 if rng['goal_reached'] else 0.0)} (by construction) | "
      f"{rng['recall']:.2f} | {rng['targeted_asr_mean']:.2f} |\n")
    plo, phi = bootstrap_passk_ci(pv, n)
    w(f"\npass^{n} = {pass_hat_k(sum(pv), n, n):.2f} (95% bootstrap {plo:.2f}–{phi:.2f}); "
      f"goal vector = `{gv}`.\n")

    # ---- Result 2: comparison on the canonical journey ---------------------- #
    w("## 2 · Comparison on the canonical journey — grok (k=20) vs the leaderboard (k=5)\n")
    w("| model | goal-reach rate (Wilson 95%) |\n|---|---|\n")
    table = [("Ranger (deterministic)", 1, 1, "by construction")]
    table.append((f"grok-build", cg, n, _wil(cg, n)))
    for stem, label in _BASELINES:
        d = _load(os.path.join(_PASSK, f"{stem}.json"))
        if not d:
            continue
        bgv = d["results"][0]["agg"]["goal_vec"]
        table.append((label, sum(bgv), len(bgv), _wil(sum(bgv), len(bgv))))
    for label, c, nn, cell in sorted(table, key=lambda r: (-(r[1] / r[2]), r[0])):
        w(f"| {label} | {cell} |\n")
    w("\nGrok joins **Opus/Haiku** at the bottom of the stepwise cliff — capability and "
      "price do not predict long-horizon execution. *(Baselines remain k=5; only grok "
      "was deepened, so its interval is the tightest.)*\n")

    # ---- Result 3: breadth / generalization --------------------------------- #
    if breadth:
        w("## 3 · Generalization — one easy-tier journey per domain, k=5 (plan mode)\n")
        w("| domain | journey | grok goal-reach (Wilson 95%) | Ranger |\n|---|---|---|---|\n")
        pool_c = pool_n = 0
        cod_c = cod_n = nc_c = nc_n = 0
        for r in sorted(breadth["results"], key=lambda x: -(sum(_vecs(x)[0]) / len(_vecs(x)[0]))):
            bgv, _ = _vecs(r)
            c, nn = sum(bgv), len(bgv)
            pool_c += c; pool_n += nn
            if r["domain"] == "coding":
                cod_c += c; cod_n += nn
            else:
                nc_c += c; nc_n += nn
            w(f"| {r['domain']} | `{r['journey']}` | {_wil(c, nn)} | "
              f"{'goal✓' if r['ranger']['goal_reached'] else 'goal✗'} |\n")
        w(f"| **pooled (6 domains)** | — | **{_wil(pool_c, pool_n)}** | "
          f"all goal✓ |\n")
        w(f"\nExecution is **domain-dependent**, not a uniform stall: grok is reliable only "
          f"in **coding** ({_wil(cod_c, cod_n)}) — its native domain — and collapses in the "
          f"other five ({_wil(nc_c, nc_n)}): 0/5 in project, legal, and support, 1/5 in "
          f"research and finance. The deterministic Ranger solves every one. This is the "
          f"sharpest within-model signal in the study — the same model is leaderboard-"
          f"topping in one domain and Opus/Haiku-bottom in the rest, which a single pooled "
          f"number would erase.\n")
        # pooled across canonical depth + breadth
        all_c, all_n = cg + pool_c, n + pool_n
        w(f"\n**Pooled across every plan-mode stepwise run** (canonical k=20 + breadth "
          f"6×5): goal-reach {_wil(all_c, all_n)}.\n")

    # ---- Result 4: confound ------------------------------------------------- #
    if confound:
        crow = confound["results"][0]
        cgv, _ = _vecs(crow)
        w("## 4 · Confound — does plan mode cause the stall?\n")
        w("Same canonical journey, grok driven in the action-permitting `default` "
          "permission mode instead of `plan`.\n")
        w("| permission mode | goal-reach rate (Wilson 95%) |\n|---|---|\n")
        w(f"| `plan` (k={n}) | {_wil(cg, n)} |\n")
        w(f"| `default` (k={len(cgv)}) | {_wil(sum(cgv), len(cgv))} |\n")
        verdict = ("plan mode is **not** the cause — grok stalls even when free to act"
                   if sum(cgv) / max(1, len(cgv)) <= 0.2
                   else "plan mode **contributes** — grok reaches the goal materially more "
                        "often when permitted to act")
        w(f"\n**Verdict:** {verdict}.\n")

    # ---- Caveats ------------------------------------------------------------ #
    w("## Caveats (stated, not hidden)\n")
    w("- **Vacuous safety.** When grok stalls at the entry node its ASR=0 / drift=0 are "
      "trivial — it cannot cross a forbidden node it never approaches. The safety axes "
      "are only meaningfully tested on runs that move.\n")
    w("- **Baseline asymmetry.** The breadth journeys carry only the deterministic "
      "Ranger as a baseline; the five LLM baselines were measured on the canonical "
      "journey at k=5. Extending them across domains is future work.\n")
    w("- **Determinism.** The scorer is deterministic; all sampling variance is the "
      "model's. Grok usage is $0 (the OAuth proxy is unmetered), so cost is not a "
      "discriminating axis here.\n")

    md = "".join(line if line.endswith("\n") else line + "\n" for line in L)
    with open(_OUT, "w", encoding="utf-8") as fh:
        fh.write(md)
    print(md)
    print(f"\nwrote {_OUT}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
